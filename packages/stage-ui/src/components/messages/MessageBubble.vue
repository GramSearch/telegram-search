<script setup lang="ts">
import type { CoreMessage } from '@tg-search/core/types'

import { computed } from 'vue'

import Avatar from '../ui/Avatar.vue'
import MediaRenderer from './MediaRenderer.vue'

const props = defineProps<{
  message: CoreMessage
}>()

const formattedTimestamp = computed(() => {
  if (!props.message.platformTimestamp)
    return ''
  return new Date(props.message.platformTimestamp).toLocaleString()
})
</script>

<template>
  <div class="flex items-start gap-4 rounded-lg p-3 transition-all duration-200">
    <div class="mt-1">
      <Avatar
        :name="message.fromName"
        size="md"
      />
    </div>
    <div class="flex-1">
      <div class="mb-1 flex items-center gap-2">
        <span class="whitespace-nowrap text-primary font-medium">{{ message.fromName }}</span>
        <span class="whitespace-nowrap text-xs text-secondary-foreground">{{ formattedTimestamp }}</span>
        <span class="whitespace-nowrap text-xs text-secondary-foreground">{{ message.platformMessageId }}</span>
      </div>

      <div class="text-foreground">
        <MediaRenderer :message="message" />
      </div>
    </div>
  </div>
</template>
